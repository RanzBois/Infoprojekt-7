    private void platziereZufaellig(int count, int screenWidth, int screenHeight) {

        if (count < 0) {
            return;
        }

        reset();

        graph = new Graph(screenWidth, screenHeight);
        graph.init(count);
        graph.fillRandomly();
        
    }

    private void drawGraph() {
        
        graph.draw(nodePanel.getGraphics());
        
    }

